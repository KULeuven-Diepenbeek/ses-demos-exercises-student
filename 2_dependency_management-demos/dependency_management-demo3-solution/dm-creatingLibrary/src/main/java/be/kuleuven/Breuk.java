package be.kuleuven;

public class Breuk {
    private int teller;
    private int noemer;

    public Breuk(int t, int n){
        this.teller = t;
        this.noemer = n;
    }

    public int getTeller() {
        return teller;
    }

    public int getNoemer() {
        return noemer;
    }
}

